const LANGS = ["vi", "en", "zh"];

function getLang() {
  const saved = localStorage.getItem("hm_lang");
  return LANGS.includes(saved) ? saved : "vi";
}

function t(key, lang = getLang()) {
  return I18N[lang][key] ?? I18N.vi[key] ?? key;
}

function fmtPrice(n, lang = getLang()) {
  const locale = lang === "vi" ? "vi-VN" : lang === "zh" ? "zh-CN" : "en-US";
  return new Intl.NumberFormat(locale).format(n) + " ₫";
}

function applyI18n() {
  const lang = getLang();
  document.documentElement.lang = lang === "zh" ? "zh-CN" : lang;
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.innerHTML = t(el.dataset.i18n, lang);
  });
  const page = document.body.dataset.page;
  if (page) document.title = t(`meta.title.${page}`, lang);
  document.querySelectorAll(".lang-btn").forEach((b) =>
    b.classList.toggle("active", b.dataset.lang === lang)
  );
}

function setLang(lang) {
  localStorage.setItem("hm_lang", lang);
  applyI18n();
  document.dispatchEvent(new CustomEvent("hm:langchange"));
}

document.addEventListener("DOMContentLoaded", () => {
  document.querySelectorAll(".lang-btn").forEach((b) =>
    b.addEventListener("click", () => setLang(b.dataset.lang))
  );
  applyI18n();
});
